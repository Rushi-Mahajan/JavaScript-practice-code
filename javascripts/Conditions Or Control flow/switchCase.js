let Month="Mar"

switch (Month) {
    case "Jan":
        console.log("Jan");
        break;
    case "Feb":
        console.log("Feb");
        break;
    case "Mar":
        console.log("March");
        break;
    case "Apr":
        console.log("Apr");
        break;
    case "May":
        console.log("May");
        break;
    default:
        console.log("Invalid Month");
        break;
}